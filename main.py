@app.route('/register', methods=['GET', 'POST'])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=form.email.data).first():
            flash('You have already signed up with this email. Please log in.')
            return redirect(url_for('login'))
            
        secure_password = generate_password_hash(password=form.password.data, method='pbkdf2:sha256', salt_length=8)
        new_user = User(
            name = form.username.data,
            email = form.email.data,
            password = secure_password
        )
        db.session.add(new_user)
        db.session.commit()
        flash(f"User created. Welcome to my blog {new_user.name}!")

        login_user(new_user)
        return redirect(url_for('get_all_posts'))

    return render_template("register.html", form=form)


@app.route('/login', methods=['GET', 'POST'])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        user = User.query.filter_by(email=form.email.data).first()
        if user != None:
            if check_password_hash(user.password, form.password.data):
                login_user(user)
                flash(f'You are logged in! Welcome back {current_user.name}:)')
                return redirect(url_for('get_all_posts'))
            else:
                flash("Error! Wrong password!")
        else:
            flash("Error! Wrong username!")
    return render_template("login.html", form=form)


@app.route('/logout')
def logout():
    flash('You are logged out!')
    logout_user()
    return redirect(url_for('get_all_posts'))

